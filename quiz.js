function shuffle(arr){
  const a=[...arr];
  for(let i=a.length-1;i>0;i--){
    const j=Math.floor(Math.random()*(i+1));
    [a[i],a[j]]=[a[j],a[i]];
  }
  return a;
}

function pickQuestions(){
  const easy=questionBank.filter(q=>q.level==='سهل');
  const mid=questionBank.filter(q=>q.level==='متوسط');
  const hard=questionBank.filter(q=>q.level==='صعب');
  return shuffle([...shuffle(easy).slice(0,2), ...shuffle(mid).slice(0,2), ...shuffle(hard).slice(0,1)]);
}

function escapeHtml(s){return String(s).replace(/[&<>]/g,m=>({'&':'&amp;','<':'&lt;','>':'&gt;'}[m]));}

function drawQuiz(){
  currentQuestions = pickQuestions();
  answered = {};
  const area=document.getElementById('quizArea');
  area.innerHTML = currentQuestions.map((q,i)=>`
    <article class="question-card" id="qcard-${i}">
      <div class="question-meta"><span>${q.category}</span><span>${q.level}</span></div>
      <h3>${i+1}. ${escapeHtml(q.q)}</h3>
      <div class="hint-box" id="hint-${i}" hidden>💡 ${escapeHtml(q.hint)}</div>
      <div class="options-grid">
        ${q.options.map((o,j)=>`<button class="option-btn" data-i="${i}" data-j="${j}">${escapeHtml(o)}</button>`).join('')}
      </div>
      <button class="mini-hint" data-hint="${i}">إظهار تلميح</button>
      <div class="answer-feedback" id="feedback-${i}"></div>
    </article>`).join('');
  document.getElementById('quizResult').innerHTML='';
  document.querySelectorAll('.option-btn').forEach(btn=>btn.addEventListener('click',chooseAnswer));
  document.querySelectorAll('.mini-hint').forEach(btn=>btn.addEventListener('click',()=>{
    document.getElementById('hint-'+btn.dataset.hint).hidden=false;
  }));
}

function chooseAnswer(e){
  const i=Number(e.currentTarget.dataset.i);
  const j=Number(e.currentTarget.dataset.j);
  if(answered[i]!==undefined) return;
  answered[i]=j;
  const q=currentQuestions[i];
  const card=document.getElementById('qcard-'+i);
  const buttons=card.querySelectorAll('.option-btn');
  buttons.forEach((b,idx)=>{
    b.disabled=true;
    if(idx===q.answer) b.classList.add('correct');
    if(idx===j && j!==q.answer) b.classList.add('wrong');
  });
  const ok=j===q.answer;
  document.getElementById('feedback-'+i).innerHTML = ok
    ? `<div class="feedback-ok">✅ إجابة صحيحة<br><small>${escapeHtml(q.explain)}</small></div>`
    : `<div class="feedback-bad">❌ إجابة خاطئة<br>الإجابة الصحيحة: <b>${escapeHtml(q.options[q.answer])}</b><br><small>${escapeHtml(q.explain)}</small></div>`;
  updateResult();
}

function updateResult(){
  const total=currentQuestions.length;
  const count=Object.keys(answered).length;
  const score=currentQuestions.reduce((s,q,i)=>s+(answered[i]===q.answer?1:0),0);
  const result=document.getElementById('quizResult');
  result.innerHTML=`<div class="result-card"><h2>${score}/${total}</h2><p>تمت الإجابة على ${count} من ${total}</p>${count===total?'<b>'+(score>=4?'ممتاز جدًا ✅':'راجع الدروس ثم أعد المحاولة 📘')+'</b>':''}</div>`;
  if(count===total){
    const best=Number(localStorage.getItem('bestQuizScore')||0);
    if(score>best) localStorage.setItem('bestQuizScore',score);
    loadBest();
  }
}

function loadBest(){
  const best=localStorage.getItem('bestQuizScore')||0;
  const el=document.getElementById('savedBest');
  if(el) el.textContent=best+'/5';
}

document.getElementById('newQuiz')?.addEventListener('click',drawQuiz);
loadBest();
drawQuiz();
